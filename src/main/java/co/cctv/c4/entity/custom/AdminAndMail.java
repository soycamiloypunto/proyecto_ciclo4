package co.cctv.c4.entity.custom;

/**
 *
 * @author camilo
 */
public class AdminAndMail {
    private Integer id;
    private String email;
    private String password;
    private String name;

    public AdminAndMail() {
    }

    public AdminAndMail(Integer id, String email, String password, String name) {
        this.id = id;
        this.email = email;
        this.password = password;
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    
    
    
}
